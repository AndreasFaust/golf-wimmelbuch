const localesDir = path.join(import.meta.dirname, "../i18n/locales");

for (const file of fs.readdirSync(localesDir).filter((f) => f.endsWith(".json"))) {
  const locale = file.replace(/\.json$/, "");
  const labels = COUNTRY_LABELS[locale];
  if (!labels) {
    console.warn(`Skipping ${file}: no country labels defined`);
    continue;
  }

  const filePath = path.join(localesDir, file);
  const data = JSON.parse(fs.readFileSync(filePath, "utf8"));
  data.requestForm.countries = labels;
  fs.writeFileSync(filePath, `${JSON.stringify(data, null, 2)}\n`);
  console.log(`Updated ${file}`);
}
